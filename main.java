public class main {
    public static void main(String[] args) {
        int x = 123; //initialization
        long y = 123456789;
        float f = 3.14f;
        double d = 3.14;
        boolean b = true;
        char s = '@';
        String name = "Damon";

        System.out.println("My number is x: " + x);
        System.out.println("long number: " + y);
        System.out.println("float pie: " + f);
        System.out.println("double pie: " + d);
        System.out.println("boolean true: " + b);
        System.out.println("char symbol: " + s);
        System.out.println("Hello" + name);

    }
}
